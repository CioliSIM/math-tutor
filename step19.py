    print(f"")
    print(f"  STATEMENT:")
    print(f"  For all n ≥ 1:")
    print(f"  1² + 2² + 3² + ... + n² = n(n+1)(2n+1)/6")
    print(f"")
    print(f"  --- ANALYSIS ---")
    print(f"  GOAL: ∀n≥1, P(n) where P(n) is the formula.")
    print(f"  FORM: universal over natural numbers → induction.")
    print(f"")
    print(f"  The sum up to n+1 = (sum up to n) + (n+1)².")
    print(f"  This recursive structure is exactly what induction exploits.")
    print(f"")
    print(f"  Key algebraic step to verify:")
    print(f"  n(n+1)(2n+1)/6 + (n+1)² = (n+1)(n+2)(2n+3)/6")
    print(f"  Factor out (n+1)/6, then simplify.")
    print(f"")
    print(f"  --- THE PROOF ---")
    print(f"")
    print(f"  We prove by induction on n.")
    print(f"")
    print(f"  Base case (n=1):")
    print(f"  Left side: 1² = 1.")
    print(f"  Right side: 1·2·3/6 = 6/6 = 1. ✓")
    print(f"")
    print(f"  Inductive step:")
    print(f"  Suppose 1²+...+k² = k(k+1)(2k+1)/6  for some k≥1. [IH]")
    print(f"  We show 1²+...+k²+(k+1)² = (k+1)(k+2)(2k+3)/6.")
    print(f"")
    print(f"  1²+...+k²+(k+1)²")
    print(f"  = k(k+1)(2k+1)/6 + (k+1)²              [by IH]")
    print(f"  = (k+1) · [k(2k+1)/6 + (k+1)]          [factor (k+1)]")
    print(f"  = (k+1) · [k(2k+1) + 6(k+1)] / 6       [common denominator]")
    print(f"  = (k+1) · [2k²+k+6k+6] / 6")
    print(f"  = (k+1) · [2k²+7k+6] / 6")
    print(f"  = (k+1)(k+2)(2k+3) / 6                  [factor 2k²+7k+6]")
    print(f"  = (k+1)((k+1)+1)(2(k+1)+1) / 6 ✓")
    print(f"")
    print(f"  By induction, the formula holds for all n ≥ 1. □")
    print(f"")
    print(f"  --- REFLECTION ---")
    print(f"  The hard part is the algebra in the inductive step.")
    print(f"  Always factor out (k+1) first — it's the key move.")
    print(f"  Then verify 2k²+7k+6 = (k+2)(2k+3) by expanding.")
    print(f"")
    print(f"  Important: induction PROVES the formula but doesn't EXPLAIN")
