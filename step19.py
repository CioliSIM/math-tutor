def composition_inverses():
    print(f"\n{'='*50}")
    print(f"COMPOSITION AND INVERSES")
    print(f"{'='*50}")
    print(f"")
    print(f"  COMPOSITION:")
    print(f"  If f: A→B and g: B→C,")
    print(f"  then g∘f: A→C means 'apply f first, then g'.")
    print(f"  (g∘f)(a) = g(f(a))")
    print(f"")
    print(f"  Key theorems:")
    print(f"  · f and g both injective  → g∘f injective")
    print(f"  · f and g both surjective → g∘f surjective")
    print(f"  · g∘f injective  → f injective")
    print(f"  · g∘f surjective → g surjective")
    print(f"")
    print(f"  PROOF (f,g injective → g∘f injective):")
    print(f"  Suppose f and g are injective.")
    print(f"  Suppose (g∘f)(a₁) = (g∘f)(a₂).")
    print(f"  Then g(f(a₁)) = g(f(a₂)).")
    print(f"  Since g is injective: f(a₁) = f(a₂).")
    print(f"  Since f is injective: a₁ = a₂.")
    print(f"  Therefore g∘f is injective. □")
