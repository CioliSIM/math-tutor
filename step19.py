def problem_finite_injection():
    print(f"\n{'='*50}")
    print(f"PROBLEM 7: INJECTIVE ↔ SURJECTIVE ON FINITE SETS")
    print(f"{'='*50}")
    print(f"")
    print(f"  STATEMENT:")
    print(f"  Let f: {{1,...,n}} → {{1,...,n}}.")
    print(f"  Prove: f is injective if and only if f is surjective.")
    print(f"")
    print(f"  On INFINITE sets this is FALSE.")
    print(f"  f: ℕ→ℕ, f(n)=n+1 is injective but NOT surjective (1 is never an output).")
    print(f"  The finiteness is essential.")
    print(f"")
    print(f"  --- ANALYSIS ---")
    print(f"  GOAL: injective ↔ surjective.")
    print(f"  FORM: biconditional → prove both directions.")
    print(f"")
    print(f"  (→): injective → surjective")
    print(f"  If f is injective, the n inputs produce n DISTINCT outputs.")
    print(f"  n distinct elements from an n-element set = the whole set.")
    print(f"  So every element of {{1,...,n}} is an output → surjective.")
    print(f"")
    print(f"  (←): surjective → injective")
    print(f"  If f is surjective, every output is hit at least once.")
    print(f"  n outputs, n inputs. If any output were hit twice,")
    print(f"  some other output would be hit zero times → not surjective.")
    print(f"  Contradiction. So every output is hit exactly once → injective.")
    print(f"")
    print(f"  --- THE PROOF ---")
    print(f"")
    print(f"  (→) Suppose f is injective.")
    print(f"  Since f: {{1,...,n}} → {{1,...,n}} is injective,")
    print(f"  f(1), f(2), ..., f(n) are n distinct elements of {{1,...,n}}.")
    print(f"  But {{1,...,n}} has exactly n elements.")
    print(f"  So {{f(1),...,f(n)}} = {{1,...,n}}.")
    print(f"  Every element of {{1,...,n}} is in the range of f.")
    print(f"  Therefore f is surjective. □")
    print(f"")
    print(f"  (←) Suppose f is surjective.")
    print(f"  Suppose for contradiction that f is NOT injective.")
    print(f"  Then f(i) = f(j) for some i ≠ j.")
    print(f"  The range of f has at most n-1 distinct values")
    print(f"  (since two inputs share one output).")
    print(f"  Therefore |range(f)| ≤ n-1 < n = |{{1,...,n}}|.")
    print(f"  So f is not surjective — contradiction.")
    print(f"  Therefore f is injective. □")
    print(f"")
    print(f"  --- REFLECTION ---")
    print(f"  The key: on a finite set, n distinct things from an n-element")
    print(f"  set fills the entire set. This is obvious but powerful.")
    print(f"  On infinite sets, 'infinite distinct things from an infinite set'")
    print(f"  does NOT fill the whole set — that's what makes them different.")
    print(f"  Hilbert's hotel shows this beautifully.")


