def quantifiers():
    print(f"\n{'='*50}")
    print(f"QUANTIFIERS")
    print(f"{'='*50}")
    print(f"")
    print(f"  Most mathematical statements are about COLLECTIONS of objects.")
    print(f"  'For ALL integers n, ...'")
    print(f"  'There EXISTS a prime p such that ...'")
    print(f"  The words 'for all' and 'there exists' are QUANTIFIERS.")
    print(f"  They are everywhere in mathematics.")
    print(f"")
    print(f"  UNIVERSAL QUANTIFIER  ∀  ('for all', 'for every')")
    print(f"  ∀x P(x) means: P(x) is true for EVERY possible x.")
    print(f"")
    print(f"  To PROVE ∀x P(x):")
    print(f"  Take an ARBITRARY x — one with no special properties.")
    print(f"  Show P(x) holds. Since x was arbitrary, it holds for all x.")
    print(f"")
    print(f"  To DISPROVE ∀x P(x):")
    print(f"  Find ONE specific x where P(x) is false.")
    print(f"  This is called a COUNTEREXAMPLE.")
    print(f"  One counterexample is enough to destroy a 'for all' claim.")
    print(f"")
    print(f"  Example: ∀n∈ℤ, n² ≥ 0")
    print(f"  Proof: let n be any integer.")
    print(f"  If n≥0: n²=n·n≥0. If n<0: n²=(-n)·(-n)≥0. Done. □")
    print(f"")
    print(f"  ─────────────────────────────────────────────")
    print(f"  EXISTENTIAL QUANTIFIER  ∃  ('there exists', 'there is')")
    print(f"  ∃x P(x) means: there is AT LEAST ONE x for which P(x) is true.")
    print(f"")
    print(f"  To PROVE ∃x P(x):")
    print(f"  Find ONE specific x that works and verify P(x).")
    print(f"  You don't need to find all solutions — just one.")
    print(f"")
    print(f"  To DISPROVE ∃x P(x):")
    print(f"  Show that P(x) is false for ALL x.")
    print(f"  (This is harder — you must prove a universal.)")
    print(f"")
    print(f"  Example: ∃n∈ℤ, n² = n")
    print(f"  Proof: take n=1. Then n²=1²=1=n. Done. □  (n=0 also works)")
    print(f"")
    print(f"  ─────────────────────────────────────────────")
    print(f"  NEGATING QUANTIFIERS — De Morgan for quantifiers:")
    print(f"")
    print(f"  ¬(∀x P(x))  ≡  ∃x ¬P(x)")
    print(f"  'Not everyone passed' = 'Someone failed'")
    print(f"")
    print(f"  ¬(∃x P(x))  ≡  ∀x ¬P(x)")
    print(f"  'Nobody passed' = 'Everyone failed'")
    print(f"")
    print(f"  This rule is crucial. To negate a 'for all', write 'there exists not'.")
    print(f"  To negate a 'there exists', write 'for all not'.")
    print(f"")
    print(f"  ─────────────────────────────────────────────")
    print(f"  ORDER OF QUANTIFIERS MATTERS — a lot:")
    print(f"")
    print(f"  ∀x ∃y (y > x)")
    print(f"  'For every number, there exists a larger one.'")
    print(f"  TRUE — for any x, take y = x+1.")
    print(f"")
    print(f"  ∃y ∀x (y > x)")
    print(f"  'There is one number larger than ALL numbers.'")
    print(f"  FALSE — no largest number exists.")
    print(f"")
    print(f"  Same words, completely different meaning, just different order.")
    print(f"  Always read quantifiers from LEFT to RIGHT.")
    print(f"")
    print(f"  ─────────────────────────────────────────────")
    print(f"  UNIQUE EXISTENCE  ∃!")
    print(f"  ∃!x P(x) means: exactly ONE x satisfies P(x).")
    print(f"  To prove it, you need TWO things:")
    print(f"  1. EXISTENCE: find one x with P(x).")
    print(f"  2. UNIQUENESS: if P(x) and P(y), then x = y.")
    print(f"")
    print(f"  Example: ∃! real x such that x + 3 = 7")
    print(f"  Existence: x=4 works (4+3=7). ✓")
    print(f"  Uniqueness: if x+3=7 and y+3=7, then x=7-3=y. ✓")
    print(f"  Therefore there is exactly one solution. □")


