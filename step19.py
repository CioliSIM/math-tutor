    print(f"  · '|' on ℤ:    a | b means a divides b")
    print(f"  · '=' is also a relation — the strictest one")
    print(f"  · 'has the same birthday as' on a set of people")
    print(f"")
    print(f"  THREE PROPERTIES:")
    print(f"")
    print(f"  REFLEXIVE: every element is related to itself.")
    print(f"  ∀a, a ~ a")
    print(f"  '≤' is reflexive: a ≤ a always. ✓")
    print(f"  '<' is NOT reflexive: a < a is always false. ✗")
    print(f"")
    print(f"  SYMMETRIC: if a ~ b then b ~ a.")
    print(f"  '=' is symmetric. '≤' is NOT (5≤7 but 7≰5).")
    print(f"  'has the same birthday as' is symmetric. ✓")
    print(f"")
    print(f"  TRANSITIVE: if a~b and b~c then a~c.")
    print(f"  '≤' is transitive. ✓")
    print(f"  'is perpendicular to' is NOT transitive.")
    print(f"  (L₁⊥L₂ and L₂⊥L₃ means L₁ ∥ L₃ — not perpendicular!)")
    print(f"")
    print(f"  EQUIVALENCE RELATION = all three properties hold.")
    print(f"  An equivalence relation groups elements into")
    print(f"  EQUIVALENCE CLASSES — clusters of mutually related elements.")
    print(f"  'Has the same birthday' partitions people by birthday.")
    print(f"")
    print(f"  Classic example: congruence modulo n.")
    print(f"  a ≡ b (mod n)  means  n | (a-b)")
    print(f"")
    print(f"  PROOF that ≡ (mod n) is an equivalence relation:")
    print(f"")
    print(f"  Reflexive: a ≡ a (mod n)?")
    print(f"  Need: n | (a-a) = 0. Yes — n | 0 for any n. ✓")
    print(f"")
    print(f"  Symmetric: if a ≡ b (mod n), then b ≡ a (mod n)?")
    print(f"  Suppose n | (a-b). Then a-b = nk.")
    print(f"  So b-a = -nk = n(-k). Since -k∈ℤ, n | (b-a). ✓")
