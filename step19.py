def equivalence_relations():
    print(f"\n{'='*50}")
    print(f"RELATIONS AND EQUIVALENCE RELATIONS")
    print(f"{'='*50}")
    print(f"")
    print(f"  A relation on a set A is a rule that says,")
    print(f"  for each pair of elements, whether they are related or not.")
    print(f"  We write a ~ b or aRb to mean 'a is related to b'.")
    print(f"")
    print(f"  Examples of relations:")
