    print(f"")
    print(f"  STATEMENT:")
    print(f"  For all n ≥ 1 and 1 ≤ k ≤ n:")
    print(f"  C(n,k) = C(n-1,k-1) + C(n-1,k)")
    print(f"")
    print(f"  This is the rule behind Pascal's triangle.")
    print(f"  Each entry = sum of the two directly above it.")
    print(f"")
    print(f"  --- ANALYSIS: TWO APPROACHES ---")
    print(f"")
    print(f"  APPROACH 1 — Algebra:")
    print(f"  Expand C(n,k) = n!/(k!(n-k)!) and verify.")
