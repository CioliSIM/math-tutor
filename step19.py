    equivs = [
        ("Double negation",
         "¬¬P ≡ P",
         "Negating twice gives back the original.",
         "'Not not even' = 'even'. Always."),

        ("De Morgan's Law 1",
         "¬(P ∧ Q) ≡ ¬P ∨ ¬Q",
         "To negate AND, flip to OR and negate each part.",
         "'Not (raining AND cold)' = 'not raining OR not cold'."),

        ("De Morgan's Law 2",
         "¬(P ∨ Q) ≡ ¬P ∧ ¬Q",
         "To negate OR, flip to AND and negate each part.",
         "'Not (raining OR windy)' = 'not raining AND not windy'."),

        ("Contrapositive",
         "P → Q  ≡  ¬Q → ¬P",
         "An implication and its contrapositive say the same thing.",
         "Proved by the truth table above."),

        ("Implication as OR",
         "P → Q  ≡  ¬P ∨ Q",
         "Every IF-THEN can be rewritten as an OR.",
         "'If it rains I take umbrella' = 'It doesn't rain OR I have umbrella'."),

        ("Biconditional split",
         "P ↔ Q  ≡  (P → Q) ∧ (Q → P)",
         "IFF = both directions hold.",
         "To prove IFF, always prove both implications separately."),

        ("Distributivity",
         "P ∧ (Q ∨ R) ≡ (P∧Q) ∨ (P∧R)",
         "AND distributes over OR — just like × over + in algebra.",
         ""),
    ]

    for name, formula, meaning, example in equivs:
        print(f"  {'─'*46}")
        print(f"  {name}")
        print(f"  {formula}")
