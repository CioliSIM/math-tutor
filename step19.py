    print(f"  Then a-c = (a-b)+(b-c) = nj+nk = n(j+k). ✓")
    print(f"")
    print(f"  All three hold — ≡ (mod n) is an equivalence relation. □")
    print(f"")
    print(f"  HOW TO PROVE ANY RELATION IS AN EQUIVALENCE RELATION:")
