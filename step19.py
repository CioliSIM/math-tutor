    print(f"{'='*50}")
    print(f"")
    print(f"  Goal: P ↔ Q")
    print(f"  Method: prove BOTH directions — two separate proofs.")
    print(f"  P ↔ Q means (P → Q) AND (Q → P).")
    print(f"  You cannot prove both at once. Always separate them.")
    print(f"")
    print(f"  Template:")
    print(f"  ┌─────────────────────────────────────┐")
    print(f"  │ (→) Suppose P. [steps] Therefore Q. │")
    print(f"  │ (←) Suppose Q. [steps] Therefore P. │")
    print(f"  │ Therefore P ↔ Q. □                  │")
    print(f"  └─────────────────────────────────────┘")
    print(f"")
    print(f"  Important: the two directions can use DIFFERENT techniques.")
    print(f"  One might be direct, the other contrapositive.")
    print(f"  Don't assume the same argument works both ways.")
    print(f"")
    print(f"  Example: n is even ↔ n² is even.")
    print(f"")
    print(f"  PROOF:")
    print(f"  (→) Suppose n is even. Then n = 2k.")
    print(f"      n² = (2k)² = 4k² = 2(2k²). So n² is even. ✓")
    print(f"")
    print(f"  (←) We prove the contrapositive: n odd → n² odd.")
    print(f"      Suppose n is odd. Then n = 2k+1.")
    print(f"      n² = (2k+1)² = 4k²+4k+1 = 2(2k²+2k)+1. Odd. ✓")
    print(f"")
    print(f"  Therefore n is even ↔ n² is even. □")
    print(f"")
    print(f"  Notice:")
    print(f"  · (→) was proved directly.")
    print(f"  · (←) was proved by contrapositive.")
    print(f"  · Different techniques for different directions — perfectly fine.")


