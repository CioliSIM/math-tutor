def proof_by_induction():
    print(f"\n{'='*50}")
    print(f"PROOF BY INDUCTION")
    print(f"{'='*50}")
    print(f"")
    print(f"  Goal: ∀n∈ℕ, P(n)  (P holds for all natural numbers)")
    print(f"")
    print(f"  Induction is the technique built specifically for this.")
    print(f"  Think of it as dominoes:")
    print(f"  · The first domino falls  (base case)")
    print(f"  · Each falling domino knocks over the next  (inductive step)")
    print(f"  · Therefore ALL dominoes fall")
    print(f"")
    print(f"  TWO STEPS:")
    print(f"")
    print(f"  BASE CASE: prove P(0) (or P(1), wherever you start).")
    print(f"  INDUCTIVE STEP: prove P(n) → P(n+1).")
    print(f"    Assume P(n) — this is the INDUCTIVE HYPOTHESIS.")
    print(f"    Use it to prove P(n+1).")
    print(f"")
    print(f"  Template:")
    print(f"  ┌─────────────────────────────────────┐")
    print(f"  │ Base case: [prove P(0)]              │")
    print(f"  │ Inductive step:                      │")
    print(f"  │   Let n be arbitrary.                │")
    print(f"  │   Suppose P(n).  [inductive hyp.]    │")
    print(f"  │   [prove P(n+1) using P(n)]          │")
    print(f"  │ By induction, ∀n P(n). □             │")
    print(f"  └─────────────────────────────────────┘")
    print(f"")
    print(f"  --- EXAMPLE 1 ---")
    print(f"  Theorem: for all n ≥ 1, 1+2+...+n = n(n+1)/2.")
    print(f"")
    print(f"  Base case (n=1):")
    print(f"  Left: 1. Right: 1·2/2 = 1. ✓")
    print(f"")
    print(f"  Inductive step:")
    print(f"  Suppose 1+2+...+k = k(k+1)/2.  [inductive hypothesis]")
    print(f"  Then:")
    print(f"  1+2+...+k+(k+1) = k(k+1)/2 + (k+1)    [by IH]")
    print(f"                   = (k+1)[k/2 + 1]")
    print(f"                   = (k+1)(k+2)/2         ✓")
    print(f"")
    print(f"  By induction, the formula holds for all n ≥ 1. □")
    print(f"")
    print(f"  --- EXAMPLE 2 ---")
    print(f"  Theorem: for all n ≥ 0, 3 | (n³-n).")
    print(f"")
    print(f"  Base case (n=0): 0³-0 = 0 = 3·0. ✓")
    print(f"")
    print(f"  Inductive step:")
    print(f"  Suppose 3 | (k³-k).  [IH]")
    print(f"  (k+1)³-(k+1) = k³+3k²+3k+1-k-1")
    print(f"               = (k³-k) + 3k²+3k")
    print(f"               = (k³-k) + 3k(k+1)")
    print(f"  3 | (k³-k) by IH. 3 | 3k(k+1) obviously.")
    print(f"  Therefore 3 | (k+1)³-(k+1). ✓")
    print(f"")
    print(f"  By induction, 3 | (n³-n) for all n ≥ 0. □")

    print(f"\n  --- Verification ---")
    print(f"  {'n':>5}  {'n³-n':>8}  {'÷3':>6}  {'✓':>4}")
    print(f"  {'─'*28}")
    for n in range(8):
        val = n**3 - n
        print(f"  {n:>5}  {val:>8}  {val//3:>6}  {'✓' if val%3==0 else '✗':>4}")


def strong_induction():
    print(f"\n{'='*50}")
    print(f"STRONG INDUCTION")
    print(f"{'='*50}")
    print(f"")
    print(f"  Regular induction: to prove P(n+1), you may use P(n).")
    print(f"  Strong induction: to prove P(n+1), you may use")
    print(f"  P(0), P(1), P(2), ..., P(n) — ALL previous cases.")
    print(f"")
    print(f"  Template:")
    print(f"  ┌─────────────────────────────────────┐")
    print(f"  │ Base case: [prove P(0)]              │")
    print(f"  │ Inductive step:                      │")
    print(f"  │   Let n be arbitrary.                │")
    print(f"  │   Suppose P(k) for ALL k < n.        │")
    print(f"  │   [prove P(n) using any P(k), k<n]  │")
    print(f"  │ By strong induction, ∀n P(n). □      │")
    print(f"  └─────────────────────────────────────┘")
    print(f"")
    print(f"  Strong induction is NOT more powerful than regular induction.")
    print(f"  They prove the same theorems.")
    print(f"  But strong induction is more CONVENIENT when P(n)")
    print(f"  depends on values earlier than just P(n-1).")
    print(f"")
    print(f"  --- EXAMPLE: every integer n ≥ 2 is a product of primes ---")
    print(f"")
    print(f"  Why strong induction?")
    print(f"  To factor n = a·b, both a and b are less than n.")
    print(f"  But a could be far less than n-1 — not just one step back.")
    print(f"  We need ALL previous cases, not just the previous one.")
    print(f"")
    print(f"  PROOF:")
    print(f"  Base case (n=2): 2 is prime, so it is a product of primes. ✓")
    print(f"")
    print(f"  Inductive step:")
    print(f"  Let n ≥ 3. Suppose every integer k with 2 ≤ k < n")
    print(f"  is a product of primes.  [strong IH]")
    print(f"")
    print(f"  Case 1: n is prime.")
    print(f"    Then n itself is a product of one prime. ✓")
    print(f"")
    print(f"  Case 2: n is composite.")
    print(f"    Then n = a·b with 2 ≤ a,b < n.")
    print(f"    By the strong IH, a = p₁·...·pⱼ and b = q₁·...·qₖ.")
    print(f"    Therefore n = p₁·...·pⱼ·q₁·...·qₖ. ✓")
    print(f"")
    print(f"  By strong induction, every n ≥ 2 is a product of primes. □")
    print(f"")
    print(f"  This proves EXISTENCE in the Fundamental Theorem of Arithmetic.")
    print(f"  (Proving UNIQUENESS of the factorization requires more work.)")


