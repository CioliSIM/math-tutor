    print(f"")
    print(f"  APPROACH 2 — Double counting (better):")
    print(f"  C(n,k) counts k-element subsets of {{1,...,n}}.")
    print(f"  Focus on whether element n is in the subset or not.")
    print(f"  Case 1: n is in → choose k-1 from {{1,...,n-1}} → C(n-1,k-1)")
    print(f"  Case 2: n is out → choose k from {{1,...,n-1}} → C(n-1,k)")
    print(f"  Total: C(n,k) = C(n-1,k-1) + C(n-1,k). □")
    print(f"")
    print(f"  Double counting = counting the same set two different ways.")
    print(f"  One count gives the left side, the other gives the right.")
    print(f"  They must be equal. Elegant and completely rigorous.")
    print(f"")
    print(f"  --- THE PROOF (combinatorial) ---")
    print(f"")
    print(f"  We count k-element subsets of {{1,...,n}}.")
    print(f"  The total count is C(n,k) by definition.")
    print(f"")
    print(f"  Split by whether n is included:")
    print(f"  · Subsets containing n: need k-1 more from {{1,...,n-1}}.")
    print(f"    Count: C(n-1, k-1).")
    print(f"  · Subsets not containing n: need k from {{1,...,n-1}}.")
    print(f"    Count: C(n-1, k).")
    print(f"")
    print(f"  These cases are mutually exclusive and exhaustive.")
    print(f"  Therefore C(n,k) = C(n-1,k-1) + C(n-1,k). □")
    print(f"")
    print(f"  --- REFLECTION ---")
    print(f"  The combinatorial proof is shorter AND more insightful.")
    print(f"  It explains WHY the identity is true.")
    print(f"  When you have a combinatorial identity, always ask:")
    print(f"  'Can I count the same thing two different ways?'")
    print(f"  This technique — double counting — is one of the most")
    print(f"  powerful tools in discrete mathematics.")

    import math
    print(f"\n  Verification:")
